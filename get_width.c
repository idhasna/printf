#include "main.h"

/**
 * get_width - it calculates the width printed .
 * @format: a formatted string for printing arguments .
 * @i: the list of printed arguments .
 * @list: the list of the arguments .
 *
 * Return: the width .
 */
int get_width(const char *format, int *i, va_list list)
{
	int curr_i;
	int width = 0;

	for (curr_i = *i + 1; format[curr_i] != '\0'; curr_i++)
	{
		if (_isdigit(format[curr_i]))
		{
			width *= 10;
			width += format[curr_i] - '0';
		}
		else if (format[curr_i] == '*')
		{
			curr_i++;
			width = va_arg(list, int);
			break;
		}
		else
			break;
	}

	*i = curr_i - 1;

	return (width);
}
